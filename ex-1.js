function add (a,b) {
    const result = a + b;
    return result;
};
function subtract (a,b) {
    const result = a - b;
    return result;
};
function mutiply (a,b) {
    const result = a * b;
    return result;
};
function divide (a,b) {
    const result = a/b;
    return result;
};